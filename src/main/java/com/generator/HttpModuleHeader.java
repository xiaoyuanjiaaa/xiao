package com.generator;

import java.util.ArrayList;
import java.util.List;

public class HttpModuleHeader {
	private String module;
	/**
	 * @return the module
	 */
	public String getModule() {
		return module;
	}
	/**
	 * @param module the module to set
	 */
	public void setModule(String module) {
		this.module = module;
	}
	public List<MethodData> propertiesEndList=new ArrayList<MethodData>();
	
	public List<MethodData> propertiesNoList=new ArrayList<MethodData>();
	/**
	 * @return the propertiesNoList
	 */
	public List<MethodData> getPropertiesNoList() {
		return propertiesNoList;
	}
	/**
	 * @param propertiesNoList the propertiesNoList to set
	 */
	public void setPropertiesNoList(List<MethodData> propertiesNoList) {
		this.propertiesNoList = propertiesNoList;
	}
	/**
	 * @return the propertiesEndList
	 */
	public List<MethodData> getPropertiesEndList() {
		return propertiesEndList;
	}
	/**
	 * @param propertiesEndList the propertiesEndList to set
	 */
	public void setPropertiesEndList(List<MethodData> propertiesEndList) {
		this.propertiesEndList = propertiesEndList;
	}
	

}
